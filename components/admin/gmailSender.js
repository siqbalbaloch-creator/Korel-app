export function buildEmailBody({
  note,
  linkedinPost,
  twitterPost,
  newsletter,
  firstName,
  company,
  interviewSource,
  interviewTopic,
  specificMoment,
  demoLink,
  yourName,
  include = { linkedin: true, twitter: true, newsletter: true },
}) {
  // Extract first paragraph of LinkedIn post as the preview snippet
  const linkedinPreview = linkedinPost
    ? linkedinPost.split("\n\n")[0].trim()
    : "";

  // Replace all template variables in the note
  const rendered = (note || "")
    .replace(/\{\{first_name\}\}/g, firstName || "")
    .replace(/\{\{company\}\}/g, company || "")
    .replace(/\{\{interview_source\}\}/g, interviewSource || "the interview")
    .replace(/\{\{interview_topic\}\}/g, interviewTopic || "your journey")
    .replace(/\{\{specific_moment\}\}/g, specificMoment || "your insights")
    .replace(/\{\{linkedin_post_preview\}\}/g, linkedinPreview)
    .replace(/\{\{demo_link\}\}/g, demoLink || "https://usekorel.com/demo")
    .replace(/\{\{your_name\}\}/g, yourName || "Saqib");

  // Build the full pack section (appended after the note)
  const sections = [];

  if (include.linkedin && linkedinPost) {
    sections.push(
      `в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ\nрџ“Њ FULL LINKEDIN POST\nв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ\n\n${linkedinPost}`
    );
  }
  if (include.twitter && twitterPost) {
    sections.push(
      `в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ\nрќ•Џ  POST\nв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ\n\n${twitterPost}`
    );
  }
  if (include.newsletter && newsletter) {
    sections.push(
      `в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ\nрџ“° NEWSLETTER SECTION\nв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ\n\n${newsletter}`
    );
  }

  const packSection =
    sections.length > 0
      ? `\n\n\nP.S. вЂ” Here's the full pack in case you want to use any of it:\n\n` +
        sections.join("\n\n\n")
      : "";

  return (
    rendered +
    packSection +
    "\n\nв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ\nGenerated by Korel В· usekorel.com"
  );
}
